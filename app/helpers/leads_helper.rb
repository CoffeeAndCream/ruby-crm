module LeadsHelper
  def sortable(column, title = nil)
    title ||= column.titleize
    css_class = column == sort_column ? "current #{sort_direction}" : nil
    direction = sort_column && sort_direction == "desc" ? "asc" : "desc"
    link_to title, params.permit(:page, :sort, :direction, :search, :fullname, :customer, :city, :address, :zip, :user).merge(:sort => column, :direction => direction, :page => nil), {:class => css_class}
  end
  def flash_display
    response = ""
    flash.each do |name, msg|
      response = response + content_tag(:div, msg, :id => "flash_#{name}")
    end
    flash.discard
    response
  end
